#include "unity.h"
#include "poly1305.h"
#include "chacha20.h"
#include <string.h>

void setUp(void) {}
void tearDown(void) {}

/* RFC 7539 Section 2.5.2: Poly1305 MAC */
void test_poly1305_mac(void)
{
    const uint8_t key[32] = {
        0x85, 0xd6, 0xbe, 0x78, 0x57, 0x55, 0x6d, 0x33,
        0x7f, 0x44, 0x52, 0xfe, 0x42, 0xd5, 0x06, 0xa8,
        0x01, 0x03, 0x80, 0x8a, 0xfb, 0x0d, 0xb2, 0xfd,
        0x4a, 0xbf, 0xf6, 0xaf, 0x41, 0x49, 0xf5, 0x1b
    };

    const char *msg_str = "Cryptographic Forum Research Group";
    const uint8_t *msg = (const uint8_t *)msg_str;
    size_t msg_len = strlen(msg_str);

    const uint8_t expected_tag[16] = {
        0xa8, 0x06, 0x1d, 0xc1, 0x30, 0x51, 0x36, 0xc6,
        0xc2, 0x2b, 0x8b, 0xaf, 0x0c, 0x01, 0x27, 0xa9
    };

    poly1305_ctx ctx;
    uint8_t tag[16];

    poly1305_init(&ctx, key);
    poly1305_update(&ctx, msg, msg_len);
    poly1305_finish(&ctx, tag);

    TEST_ASSERT_EQUAL_HEX8_ARRAY(expected_tag, tag, 16);
}

/* RFC 7539 Section 2.6.2: Poly1305 Key Generation */
void test_poly1305_key_generation(void)
{
    const uint8_t key[32] = {
        0x80, 0x81, 0x82, 0x83, 0x84, 0x85, 0x86, 0x87,
        0x88, 0x89, 0x8a, 0x8b, 0x8c, 0x8d, 0x8e, 0x8f,
        0x90, 0x91, 0x92, 0x93, 0x94, 0x95, 0x96, 0x97,
        0x98, 0x99, 0x9a, 0x9b, 0x9c, 0x9d, 0x9e, 0x9f
    };
    const uint8_t nonce[12] = {
        0x00, 0x00, 0x00, 0x00, 0x00, 0x01, 0x02, 0x03,
        0x04, 0x05, 0x06, 0x07
    };

    const uint8_t expected_otk[32] = {
        0x8a, 0xd5, 0xa0, 0x8b, 0x90, 0x5f, 0x81, 0xcc,
        0x81, 0x50, 0x40, 0x27, 0x4a, 0xb2, 0x94, 0x71,
        0xa8, 0x33, 0xb6, 0x37, 0xe3, 0xfd, 0x0d, 0xa5,
        0x08, 0xdb, 0xb8, 0xe2, 0xfd, 0xd1, 0xa6, 0x46
    };

    /* Generate one-time key: chacha20_block with counter=0, take first 32 bytes */
    uint32_t block[16];
    chacha20_block(block, key, 0, nonce);

    uint8_t otk[32];
    for (int i = 0; i < 8; i++) {
        otk[4*i + 0] = (uint8_t)(block[i]);
        otk[4*i + 1] = (uint8_t)(block[i] >> 8);
        otk[4*i + 2] = (uint8_t)(block[i] >> 16);
        otk[4*i + 3] = (uint8_t)(block[i] >> 24);
    }

    TEST_ASSERT_EQUAL_HEX8_ARRAY(expected_otk, otk, 32);
}

int main(void)
{
    UNITY_BEGIN();
    RUN_TEST(test_poly1305_mac);
    RUN_TEST(test_poly1305_key_generation);
    return UNITY_END();
}

/* Helper to serialize chacha20_block output to bytes (LE) */
static void serialize_block(uint8_t out[64], const uint32_t block[16])
{
    for (int i = 0; i < 16; i++) {
        out[4*i + 0] = (uint8_t)(block[i]);
        out[4*i + 1] = (uint8_t)(block[i] >> 8);
        out[4*i + 2] = (uint8_t)(block[i] >> 16);
        out[4*i + 3] = (uint8_t)(block[i] >> 24);
    }
}

/* RFC 7539 Section 2.1.1: Quarter Round */
void test_quarter_round(void)
{
    uint32_t a = 0x11111111;
    uint32_t b = 0x01020304;
    uint32_t c = 0x9b8d6f43;
    uint32_t d = 0x01234567;

    chacha20_quarter_round(&a, &b, &c, &d);

    TEST_ASSERT_EQUAL_HEX32(0xea2a92f4, a);
    TEST_ASSERT_EQUAL_HEX32(0xcb1cf8ce, b);
    TEST_ASSERT_EQUAL_HEX32(0x4581472e, c);
    TEST_ASSERT_EQUAL_HEX32(0x5881c4bb, d);
}

/* RFC 7539 Section 2.2.1: Quarter Round on ChaCha State */
void test_quarter_round_on_state(void)
{
    uint32_t state[16] = {
        0x879531e0, 0xc5ecf37d, 0x516461b1, 0xc9a62f8a,
        0x44c20ef3, 0x3390af7f, 0xd9fc690b, 0x2a5f714c,
        0x53372767, 0xb00a5631, 0x974c541a, 0x359e9963,
        0x5c971061, 0x3d631689, 0x2098d9d6, 0x91dbd320
    };

    chacha20_quarter_round(&state[2], &state[7], &state[8], &state[13]);

    uint32_t expected[16] = {
        0x879531e0, 0xc5ecf37d, 0xbdb886dc, 0xc9a62f8a,
        0x44c20ef3, 0x3390af7f, 0xd9fc690b, 0xcfacafd2,
        0xe46bea80, 0xb00a5631, 0x974c541a, 0x359e9963,
        0x5c971061, 0xccc07c79, 0x2098d9d6, 0x91dbd320
    };

    TEST_ASSERT_EQUAL_HEX32_ARRAY(expected, state, 16);
}

/* RFC 7539 Section 2.3.2: ChaCha20 Block Function */
void test_block_function(void)
{
    const uint8_t key[32] = {
        0x00, 0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07,
        0x08, 0x09, 0x0a, 0x0b, 0x0c, 0x0d, 0x0e, 0x0f,
        0x10, 0x11, 0x12, 0x13, 0x14, 0x15, 0x16, 0x17,
        0x18, 0x19, 0x1a, 0x1b, 0x1c, 0x1d, 0x1e, 0x1f
    };
    const uint8_t nonce[12] = {
        0x00, 0x00, 0x00, 0x09, 0x00, 0x00, 0x00, 0x4a,
        0x00, 0x00, 0x00, 0x00
    };
    const uint32_t counter = 1;

    uint32_t out[16];
    chacha20_block(out, key, counter, nonce);

    uint8_t serialized[64];
    serialize_block(serialized, out);

    const uint8_t expected[64] = {
        0x10, 0xf1, 0xe7, 0xe4, 0xd1, 0x3b, 0x59, 0x15,
        0x50, 0x0f, 0xdd, 0x1f, 0xa3, 0x20, 0x71, 0xc4,
        0xc7, 0xd1, 0xf4, 0xc7, 0x33, 0xc0, 0x68, 0x03,
        0x04, 0x22, 0xaa, 0x9a, 0xc3, 0xd4, 0x6c, 0x4e,
        0xd2, 0x82, 0x64, 0x46, 0x07, 0x9f, 0xaa, 0x09,
        0x14, 0xc2, 0xd7, 0x05, 0xd9, 0x8b, 0x02, 0xa2,
        0xb5, 0x12, 0x9c, 0xd1, 0xde, 0x16, 0x4e, 0xb9,
        0xcb, 0xd0, 0x83, 0xe8, 0xa2, 0x50, 0x3c, 0x4e
    };

    TEST_ASSERT_EQUAL_HEX8_ARRAY(expected, serialized, 64);
}

